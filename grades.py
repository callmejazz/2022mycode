your_grade = int(input("What kind of percentage did you get in programming this semester?\n"))
if your_grade >= 90:
  print("You got an A, great work!")
elif your_grade >= 80:
  print("You got an B, that's pretty good")
elif your_grade >= 70:
    print("You got an C, meh that's decent")
elif your_grade >= 60:
    print("You got an D, you're on the struggle bus here")
else:
  print("You got an F, tragic")
